function translate_term($category, $value) {
    $lang = _jazan_get_lang();
    if ($lang === 'ar' || $value === null) return $value;

    static $maps = null;
    if ($maps === null) {
        $maps = [
            'education_level' => [
                'روضة' => 'Kindergarten',
                'ابتدائي' => 'Elementary',
                'متوسط' => 'Intermediate',
                'ثانوي' => 'High School',
                'مجمع' => 'Educational Complex',
            ],
            'school_type' => [
                'حكومي' => 'Public',
                'أهلي' => 'Private',
            ],
            'gender' => [
                'بنين' => 'Boys',
                'بنات' => 'Girls',
                '' => '',
            ],
        ];
    }

    $cat = strtolower(trim($category));
    if (!isset($maps[$cat])) return $value;
    return $maps[$cat][$value] ?? $value;
}

