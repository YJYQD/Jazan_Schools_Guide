function _jazan_get_lang() {
    // Allow explicit override from request (useful for AJAX calls)
    if (!empty($_REQUEST['jazan_lang'])) {
        $req = ($_REQUEST['jazan_lang'] === 'en') ? 'en' : 'ar';
        return $req;
    }
    if (session_status() === PHP_SESSION_ACTIVE && !empty($_SESSION['lang'])) {
        return $_SESSION['lang'];
    }
    if (!empty($_COOKIE['jazan_lang'])) {
        return $_COOKIE['jazan_lang'];
    }
    return 'ar';
}

