// الماتريكس الذكية المحدثة لترجمة أسماء المدارس القادمة من الداتا بيز تلقائياً
function get_localized_field($conn, $table, $id_field, $id_value, $field, $fallback = null) {
    $lang = _jazan_get_lang();
    if (!$conn) return $fallback ?? null;

    try {
        // 1. معالجة أسماء المدارس والمكاتب التعليمية
        if ($table === 'Schools' && $field === 'School_Name') {
            $stmt = $conn->prepare("SELECT name FROM School_Translations WHERE school_id = ? AND lang = ? LIMIT 1");
            if ($stmt) {
                $stmt->bind_param('is', $id_value, $lang);
                if ($stmt->execute()) {
                    $res = $stmt->get_result();
                    $r = $res->fetch_assoc();
                    if (!empty($r['name'])) { $stmt->close(); return $r['name']; }
                }
                $stmt->close();
            }

            // مصفوفة الاستبدال الفوري الحية إذا تم اختيار الإنجليزية وكانت قاعدة البيانات فارغة
            if ($lang === 'en' && !empty($fallback)) {
                if (function_exists('get_db_fixed_translations')) {
                    $dictionary = get_db_fixed_translations();
                } else {
                    $dictionary = [
                        'الابتدائية' => 'Elementary School',
                        'ابتدائية' => 'Elementary School',
                        'المتوسطة' => 'Intermediate School',
                        'متوسطة' => 'Intermediate School',
                        'الثانوية' => 'High School',
                        'ثانوية' => 'High School',
                        'مجمع' => 'Educational Complex',
                        'مدرسة' => 'School',
                        'روضة' => 'Kindergarten',
                        'الأهلية' => 'Private',
                        'أهلية' => 'Private',
                        'العالمية' => 'International',
                        'عالمية' => 'International',
                        'تحفيظ القرآن الكريم' => 'Holy Quran Memorization',
                        'للبنين' => 'for Boys',
                        'بنين' => 'Boys',
                        'للبنات' => 'for Girls',
                        'بنات' => 'Girls',
                        'الأولى' => '1st',
                        'الثانية' => '2nd',
                        'الثالثة' => '3rd'
                    ];
                }
                uksort($dictionary, function($a, $b){ return mb_strlen($b) - mb_strlen($a); });
                return strtr($fallback, $dictionary);
            }
            return $fallback;
        }

        // 2. معالجة تراجم التعليقات الحية
        if (($table === 'School_Reviews' || $table === 'School_Review' || $table === 'Reviews') && ($field === 'Comment' || $field === 'comment')) {
            $stmt = $conn->prepare("SELECT comment FROM Review_Translations WHERE review_id = ? AND lang = ? LIMIT 1");
            if ($stmt) {
                $stmt->bind_param('is', $id_value, $lang);
                if ($stmt->execute()) {
                    $res = $stmt->get_result();
                    $r = $res->fetch_assoc();
                    if (!empty($r['comment'])) { $stmt->close(); return $r['comment']; }
                }
                $stmt->close();
            }
            
            // استبدال فوري لأشهر التعليقات التجريبية
            if ($lang === 'en' && !empty($fallback)) {
                $comment_dict = [
                    'مدرسة ممتازة' => 'Excellent school',
                    'موقع رائع' => 'Great platform',
                    'جيد جداً' => 'Very good evaluation',
                    'المبنى حديث ومتطور' => 'The building is modern and advanced'
                ];
                return strtr($fallback, $comment_dict);
            }
            return $fallback;
        }

        return $fallback;
    } catch (\mysqli_sql_exception $e) {
        error_log("i18n:get_localized_field DB error: " . $e->getMessage());
        return $fallback;
    } catch (\Throwable $t) {
        error_log("i18n:get_localized_field unexpected error: " . $t->getMessage());
        return $fallback;
    }
}
